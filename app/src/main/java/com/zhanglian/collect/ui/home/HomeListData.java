package com.zhanglian.collect.ui.home;

public class HomeListData {
    private String name;
    private String add;
    private String time;
    public HomeListData(String name, String add, String time) {
        this.name = name;
        this.add = add;
        this.time = time;
    }
    public String getName() {
        return name;
    }
    public String getAdd() { return add; }
    public String getTime() {
        return time;
    }


}
