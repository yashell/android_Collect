package com.zhanglian.collect.ui.home;

public class HomeData {
    private String name;
    private int imageId;

    public HomeData(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }
    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }


}
